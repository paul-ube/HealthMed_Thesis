    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            int row = tbl_generic.getSelectedRow();
            
            if (row==-1){
                JOptionPane.showMessageDialog(null, "Please Select Generic to add");
            }else{
                String name = (tbl_generic.getModel().getValueAt(row, 0).toString());

                String sql = "INSERT INTO pivot_medicine SET"
                + " med_id = '"+id_sheet.getText()+"'"
                + " , generic_id = (SELECT generic_id FROM generic WHERE generic_desc = '"+name+"');";

                pstmt = conn.prepareStatement(sql);
                pstmt.executeUpdate();

                JOptionPane.showMessageDialog(null, "Successfully Added");

                Home.Edit.setVisible(false);
                Home.Edit.setVisible(true);
                this.dispose();
            }

            

        } catch (SQLException ex) {
            ex.printStackTrace();
        }

        start();
    }//GEN-LAST:event_jButton2ActionPerformed

