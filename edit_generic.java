    private void name(){
        try{
            String q = "SELECT med_name FROM med_table WHERE med_id = '"+id_sheet.getText()+"'";
            pstmt = conn.prepareStatement(q);
            rs = pstmt.executeQuery();
            while (rs.next()){
                lbl_medpurpose.setText(rs.getString("med_name"));
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
    private void start(){
        
        try{
            DefaultTableModel mod = (DefaultTableModel)tbl_generic.getModel();
            mod.setRowCount(0);
            
            String select = "SELECT generic_desc FROM generic WHERE generic_desc NOT IN (SELECT b.generic_desc FROM pivot_medicine a"
                    + " INNER JOIN generic b ON a.generic_id = b.generic_id WHERE a.med_id = '"+id_sheet.getText()+"')"
                    + " ORDER BY generic_desc";
            
            pstmt = conn.prepareStatement(select);
            rs = pstmt.executeQuery();
            
            while (rs.next()){
                
                String name = rs.getString("generic_desc");
    
               
                mod.addRow(new Object[]{name});
    
            }
            
            
        }catch (Exception e){
            e.printStackTrace();
        }
        
        
        
    }

