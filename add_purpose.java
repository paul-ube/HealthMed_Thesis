    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{

            ///// Check kung may karga /////
            String karga = txt_illname.getText();

            if (karga.isEmpty()){

                JOptionPane.showMessageDialog(null, "Textfield is empty");

            }else{

                /////// CHECK FOR REDUNDANCY ////////
                String sql = "SELECT ill_name FROM ill_table WHERE ill_name = '"+karga+"';";

                pstmt = conn.prepareStatement(sql);
                rs = pstmt.executeQuery();

                if (rs.next()){

                    JOptionPane.showMessageDialog(null, "This name is already registered");

                }else{
                    int haha = cmb_category.getSelectedIndex()+1;
                    String xql = "INSERT INTO ill_table SET"
                            + " ill_id = '"+lbl_illid.getText()+"',"
                            + " ill_name = '"+txt_illname.getText()+"',"
                            + " user_ID = '101',"
                            + " dateCreated = '"+nowna+"',"
                            + " category_id = '"+haha+"';";

                    pstmt = conn.prepareStatement(xql);
                    pstmt.executeUpdate();

                    JOptionPane.showMessageDialog(null, "Successfully Added");
                    
                    this.dispose();
                    
                    Home.refresh_purpose();

                }
            }

        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

