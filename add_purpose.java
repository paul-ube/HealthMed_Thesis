    public void start(){
        try {
           
            
            String sql = "SELECT ill_id as ayu FROM ill_table ORDER BY ill_id ASC";
            
            pstmt = conn.prepareStatement(sql);
            rs = pstmt.executeQuery();
            
            if (rs.last()){
                int a = rs.getInt("ayu");
                int x = a + 1;
                String xyz = String.valueOf(x);
                lbl_illid.setText(xyz);
            }
            
            // get the illness categories
            String category = "SELECT * from ill_category";
            pstmt = conn.prepareStatement(category);
            rs = pstmt.executeQuery();
            
            
            cmb_category.removeAllItems();
            
            while (rs.next()){
                
                String x = rs.getString("category_name");
                cmb_category.addItem(x);
                
            }
            
            
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

