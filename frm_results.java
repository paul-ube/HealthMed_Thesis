    public void refreshtbl(){
        DefaultTableModel mod = (DefaultTableModel)tbl_results.getModel();
        mod.setRowCount(0);
        
        try{
            String sql = "SELECT *"
                    + " FROM patient_tbl"
                    + " WHERE ID_no != '100001' AND (id_no LIKE '%"+txt_results.getText()+"%' OR lastname LIKE '%"+txt_results.getText()+"%' OR firstname LIKE '%"+txt_results.getText()+"%'"
                    + " OR middlename LIKE '%"+txt_results.getText()+"%')";
                    
                    pstmt = conn.prepareStatement(sql);
                    rs = pstmt.executeQuery();

                    while (rs.next()){
                        String x = rs.getString("id_no");
                        String a = rs.getString("lastname");
                        String b = rs.getString("firstname");
                        String c = rs.getString("middlename");
                        
                        
                        
                        String full = a+", "+b+" "+c+".";
                        String u = WordUtils.capitalizeFully(full);
                        String d = rs.getString("Occupation");
                        String e = rs.getString("Program");
                        if (e.equals("null")){
                            e = "  -  ";
                        }
                        
                        mod.addRow(new Object[]{x,u,d,e});
                        
                        

                    }
            
        }catch(Exception e){
            e.printStackTrace();
        }
    }

